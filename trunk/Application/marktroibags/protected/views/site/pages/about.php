<h3>History</h3>
<p>The Schools Division of Makati used to belong to the Division of Rizal Province.  The creation of the Metro Manila Commission on November 7, 1975 through Presidential Decree 824 paved the way for the restructuring of the local governments of the four (4) cities:  Manila, Pasay, Caloocan, Quezon and the thirteen (13 municipalities:  Malabon, Navotas, Valenzuela, Pasig, San Juan, Mandaluyong, Novaliches, Taguig, Pateros, Muntinlupa, Las Pinas, Para&#241;aque and Makati.  Makati was then clustered with the Division of Pasay.

The desire to improve the administration and supervision, as well as enhance the efficiency and effectiveness of educational services in the National Capital Region prompted the Hon. Secretary of Education Armand V. Fabella to request the Honorable Secretary of the Department of Budget and Management to restructure the existing four (4) divisions into nine (9) on May 25, 1993.  The request was granted on September 23, 1993.  Such restructuring was able to correct the imbalances in the supervisory span of control of the Division Superintendents and at the same time ensure closer coordination with the government units both local and regional.  This also gave birth to the Division of Makati which opened its doors to the public on June 1, 1994.Dr. Corazon D. Santiago became its first Division Superintendent.  In 1998, after Dr. Santiago was promoted as Regional Director of the National Capital Region, Ms. Elena R. Ruiz has assumed the Office of Schools Division Superintendent from 1999 to the present.</p>
